#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*src;
	char	*dest;
	int		i;

	src = (char *) s;
	i = 0;
	dest = malloc(sizeof(char) * ft_strlen(src) + 1);
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
